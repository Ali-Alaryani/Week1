
public class AverageOf3Numbers {

	    public static void main(String[] args) {
	        // define the numbers
	        double num1 = 10;
	        double num2 = 20;
	        double num3 = 30;

	        // calculate the average
	        double average = (num1 + num2 + num3) / 3;

	        // print the result
	        System.out.println("The average is: " + average);
	    }
}
